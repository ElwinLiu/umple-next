func TestParseTransitionLabel(t *testing.T) {
	tests := []struct {
		label   string
		event   string
		guard   string
		action  string
	}{
		{"turnOn", "turnOn", "", ""},
		{"open", "open", "", ""},
		{"listitem(double reserve) / {...}", "listitem(double reserve)", "", "{...}"},
		{"openBidding [condition]", "openBidding", "condition", ""},
		{"closeBids [x > y] / {...}", "closeBids", "x > y", "{...}"},
		{"bid(Bid b) [b.val > price] / {...}", "bid(Bid b)", "b.val > price", "{...}"},
	}

	for _, tt := range tests {
		event, guard, action := parseTransitionLabel(tt.label)
		if event != tt.event {
			t.Errorf("label=%q: event=%q, want %q", tt.label, event, tt.event)
		}
		if guard != tt.guard {
			t.Errorf("label=%q: guard=%q, want %q", tt.label, guard, tt.guard)
		}
		if action != tt.action {
			t.Errorf("label=%q: action=%q, want %q", tt.label, action, tt.action)
		}
	}
}

// helpers

func findSM(sms []StateMachine, name string) *StateMachine {
	for i := range sms {
		if sms[i].Name == name {
			return &sms[i]
		}
	}
	return nil
}

func findState(states []State, name string) *State {
	for i := range states {
		if states[i].Name == name {
			return &states[i]
		}
	}
	return nil
}

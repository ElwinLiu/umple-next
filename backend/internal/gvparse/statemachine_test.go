func TestParseLampSimple(t *testing.T) {
	sms, err := ParseStateDiagram(lampGV)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(sms) != 1 {
		t.Fatalf("expected 1 state machine, got %d", len(sms))
	}

	sm := sms[0]
	if sm.Name != "Lamp.status" {
		t.Errorf("expected name Lamp.status, got %s", sm.Name)
	}
	if sm.ClassName != "Lamp" {
		t.Errorf("expected className Lamp, got %s", sm.ClassName)
	}
	if len(sm.States) != 2 {
		t.Fatalf("expected 2 states, got %d", len(sm.States))
	}

	on := findState(sm.States, "On")
	off := findState(sm.States, "Off")
	if on == nil || off == nil {
		t.Fatal("expected states On and Off")
	}

	// On should be initial (start transition points to it)
	if !on.IsInitial {
		t.Error("expected On to be initial state")
	}
	if off.IsInitial {
		t.Error("expected Off to NOT be initial state")
	}

	// Check transitions
	if len(on.Transitions) != 1 {
		t.Fatalf("expected 1 transition from On, got %d", len(on.Transitions))
	}
	if on.Transitions[0].Event != "turnOff" || on.Transitions[0].NextState != "Off" {
		t.Errorf("unexpected transition from On: %+v", on.Transitions[0])
	}

	if len(off.Transitions) != 1 {
		t.Fatalf("expected 1 transition from Off, got %d", len(off.Transitions))
	}
	if off.Transitions[0].Event != "turnOn" || off.Transitions[0].NextState != "On" {
		t.Errorf("unexpected transition from Off: %+v", off.Transitions[0])
	}
}

func TestParseAuctionMultiSM(t *testing.T) {
	sms, err := ParseStateDiagram(auctionGV)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(sms) != 2 {
		t.Fatalf("expected 2 state machines, got %d", len(sms))
	}

	// Auction SM
	auction := findSM(sms, "Auction.status")
	if auction == nil {
		t.Fatal("expected Auction.status state machine")
	}
	if len(auction.States) != 3 {
		t.Fatalf("expected 3 states in Auction.status, got %d", len(auction.States))
	}

	gathering := findState(auction.States, "gathering")
	if gathering == nil {
		t.Fatal("expected gathering state")
	}
	if !gathering.IsInitial {
		t.Error("expected gathering to be initial")
	}
	if len(gathering.Transitions) != 1 || gathering.Transitions[0].Event != "open" {
		t.Errorf("expected open transition from gathering, got %+v", gathering.Transitions)
	}

	underway := findState(auction.States, "underway")
	if underway == nil {
		t.Fatal("expected underway state")
	}
	if len(underway.ExitActions) == 0 {
		t.Error("expected exit actions on underway state")
	}

	// ItemAtAuction SM
	item := findSM(sms, "ItemAtAuction.status")
	if item == nil {
		t.Fatal("expected ItemAtAuction.status state machine")
	}
	if len(item.States) != 5 {
		t.Fatalf("expected 5 states in ItemAtAuction.status, got %d", len(item.States))
	}

	// Check entry actions
	listed := findState(item.States, "listed")
	if listed == nil {
		t.Fatal("expected listed state")
	}
	if len(listed.EntryActions) == 0 {
		t.Error("expected entry actions on listed state")
	}

	// Check guard on transition
	if len(listed.Transitions) < 1 {
		t.Fatal("expected transitions from listed")
	}
	hasGuardTransition := false
	for _, tr := range listed.Transitions {
		if tr.Event == "openBidding" && tr.Guard != "" {
			hasGuardTransition = true
		}
	}
	if !hasGuardTransition {
		t.Error("expected openBidding transition with guard from listed")
	}

	// Check self-loop on openForBidding
	ofb := findState(item.States, "openForBidding")
	if ofb == nil {
		t.Fatal("expected openForBidding state")
	}
	hasSelfLoop := false
	for _, tr := range ofb.Transitions {
		if tr.NextState == "openForBidding" {
			hasSelfLoop = true
			if tr.Guard == "" {
				t.Error("expected guard on self-loop transition")
			}
			if tr.Action == "" {
				t.Error("expected action on self-loop transition")
			}
		}
	}
	if !hasSelfLoop {
		t.Error("expected self-loop transition on openForBidding")
	}

	// Check transition with action from tooltip
	created := findState(item.States, "created")
	if created == nil {
		t.Fatal("expected created state")
	}
	if len(created.Transitions) != 1 {
		t.Fatalf("expected 1 transition from created, got %d", len(created.Transitions))
	}
	if created.Transitions[0].Action == "" {
		t.Error("expected action on created->listed transition (from tooltip)")
	}
}

